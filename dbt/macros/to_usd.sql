{#
    Конвертация в USD — держим в одном месте, чтобы менять конвенцию централизованно.

    rate_to_usd в данных неоднозначна: для RUB это рублей за доллар (~65-75),
    для EUR/GBP ~0.8-1.2, USD = 1. Согласованно по всем валютам получается
    только делением (умножение дало бы 1 RUB = 65 USD). Если правильным окажется
    умножение — менять здесь. nullif страхует от деления на ноль.
#}
{% macro to_usd(amount, rate) %}
    ({{ amount }} / nullif({{ rate }}, 0))
{% endmacro %}
